#include "main.h"
/**
 * print_sign - printing sign of the number
 *
 * @n: Sign of numbers
 *
 * Return: -1 if n is less than zero
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	else
	{
		_putchar('0');
		return (0);
	}
}
